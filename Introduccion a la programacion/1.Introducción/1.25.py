numero_uno = int(input('Ingrese el primer número :'))
numero_dos = int(input('Ingrese el segundo número :'))
numero_tres = int(input('Ingrese el tercer número :'))

if numero_uno < 0:
    print('El producto de los números ingresados es :',numero_uno * numero_dos * numero_tres)
else:
    print('La suma de los números ingresados es :',numero_uno + numero_dos + numero_tres)

exit(0)

    